import React, { useState, useEffect } from 'react';
import { useAuth } from '../context/AuthContext';

const TestProfileMedia = ({ userId }) => {
  const { token } = useAuth();
  const [testData, setTestData] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  useEffect(() => {
    const fetchTestData = async () => {
      try {
        const response = await fetch(`${import.meta.env.VITE_API_URL || 'http://localhost:5001'}/api/feed/user/${userId}`, {
          headers: {
            'Authorization': `Bearer ${token}`
          }
        });

        if (!response.ok) {
          throw new Error(`HTTP error! status: ${response.status}`);
        }

        const data = await response.json();
        console.log('üîç TEST - Full API Response:', data);
        console.log('üîç TEST - Posts:', data.posts);
        console.log('üîç TEST - First post:', data.posts?.[0]);
        
        setTestData(data);
      } catch (err) {
        console.error('üîç TEST - Error:', err);
        setError(err.message);
      } finally {
        setLoading(false);
      }
    };

    if (token && userId) {
      fetchTestData();
    }
  }, [token, userId]);

  if (loading) return <div>Loading test data...</div>;
  if (error) return <div>Error: {error}</div>;
  if (!testData) return <div>No data received</div>;

  return (
    <div className="test-profile-media" style={{ padding: '20px', border: '2px solid red', margin: '20px 0' }}>
      <h2>üîç TESTING PROFILE MEDIA DATA</h2>
      
      <div>
        <h3>User Info:</h3>
        <pre style={{ background: '#f0f0f0', padding: '10px', fontSize: '12px' }}>
          {JSON.stringify(testData.user, null, 2)}
        </pre>
      </div>

      <div>
        <h3>Posts Count: {testData.posts?.length || 0}</h3>
      </div>

      {testData.posts?.map((post, index) => (
        <div key={post._id} style={{ border: '1px solid blue', margin: '10px 0', padding: '10px' }}>
          <h4>Post {index + 1}:</h4>
          <p><strong>Post ID:</strong> {post._id}</p>
          <p><strong>Content:</strong> {post.content?.substring(0, 100)}...</p>
          <p><strong>Media Object:</strong> {post.media ? 'EXISTS' : 'NULL/UNDEFINED'}</p>
          
          {post.media && (
            <>
              <p><strong>Media.images:</strong> {post.media.images?.length || 0} images</p>
              <p><strong>Media.video:</strong> {post.media.video ? 'EXISTS' : 'NULL/UNDEFINED'}</p>
              
              <details>
                <summary>Full Media Data</summary>
                <pre style={{ background: '#e0e0e0', padding: '10px', fontSize: '10px' }}>
                  {JSON.stringify(post.media, null, 2)}
                </pre>
              </details>
              
              {post.media.images?.length > 0 && (
                <div>
                  <p><strong>Image URLs:</strong></p>
                  {post.media.images.map((img, imgIndex) => (
                    <div key={imgIndex} style={{ margin: '5px 0' }}>
                      <p>Image {imgIndex + 1}: {img}</p>
                      <img 
                        src={img} 
                        alt={`Test image ${imgIndex + 1}`} 
                        style={{ maxWidth: '200px', border: '1px solid red' }}
                        onError={(e) => {
                          e.target.style.border = '2px solid red';
                          console.log('Image failed to load:', img);
                        }}
                      />
                    </div>
                  ))}
                </div>
              )}
              
              {post.media.video && (
                <div>
                  <p><strong>Video URL:</strong> {post.media.video}</p>
                  <video 
                    controls 
                    style={{ maxWidth: '300px', border: '1px solid red' }}
                    onError={(e) => {
                      e.target.style.border = '2px solid red';
                      console.log('Video failed to load:', post.media.video);
                    }}
                  >
                    <source src={post.media.video} type="video/mp4" />
                    Your browser does not support the video tag.
                  </video>
                </div>
              )}
            </>
          )}
        </div>
      ))}
    </div>
  );
};

export default TestProfileMedia;
